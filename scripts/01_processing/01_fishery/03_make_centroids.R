## SET UP ######################################################################

# Load packages ----------------------------------------------------------------

library(here)
library(sf)
library(tidyverse)

# Load data --------------------------------------------------------------------
turfs <- st_read(dsn = here("data",
                            "processed",
                            "turf_polygons.gpkg"))

## PROCESSING ##################################################################

# X ----------------------------------------------------------------------------
centroids <- turfs %>% 
  st_centroid()

## EXPORT ######################################################################

# Write centroids --------------------------------------------------------------
st_write(obj = centroids,
         dsn = here("data", "processed", "centroids.gpkg"),
         delete_dsn = TRUE)
