## SET UP ######################################################################

# Load packages ----------------------------------------------------------------
pacman::p_load(
  here,
  tidyverse
)

# Load data --------------------------------------------------------------------
landings <- readRDS(file = here("data", "processed", "annual_fishery_panel.rds"))

## PROCESSING ##################################################################

# X ----------------------------------------------------------------------------
hist_landings <- landings %>% 
  filter(year <= 2013) %>% 
  group_by(eu_rnpa, fishery) %>% 
  summarize(live_weight = mean(live_weight, na.rm = T))

## EXPORT ######################################################################

# X ----------------------------------------------------------------------------
saveRDS(obj = hist_landings,
        file = here("data", "processed", "mean_historical_eu_landings.rds"))
